export const setGradient = (idx) => {
  switch (idx) {
    case 0:
      return "blue-text-gradient";
    case 1:
      return "pink-text-gradient";
    case 2:
      return "green-text-gradient";
    case 3:
      return "red-text-gradient";
    case 4:
      return "yellow-text-gradient";
    case 5:
      return "orange-text-gradient";
    default:
      return "blue-text-gradient";
  }
}